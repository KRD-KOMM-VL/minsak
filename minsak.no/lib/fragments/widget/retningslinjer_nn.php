				<h3>Kva er innbyggarinitiativ?</h3>
				<p>Gjennom å samle underskrifter har du som innbyggar i ein kommune eller eit fylke rett til å setje ei sak på den politiske dagsorden. Du har imidlertid ikkje krav på noko bestemt utfall av saka.</p>
				<h3>Kven kan føreslå saker?</h3>
				<p>Du må bu i den kommunen eller det fylket saka gjeld for å føreslå eller skrive under på ei sak. Ungdom under 18 år kan føreslå saker.</p>
				<h3>Kor mange underskrifter treng du?</h3>
				<p>Du må samle underskrifter frå minst to prosent av innbyggarane, eller minimum 300 personar i kommunen/500 personar i fylket.</p>
				<h3>Kva kan du fremje saker om?</h3>
				<p>Du kan fremje saker som gjeld kommunen eller fylkeskommunen si verksemd. Dette vil i praksis seie oppgåver som kommunen eller fylkeskommunen er engasjert i, både lovpålagde og frivillege oppgåver. Det er dermed snakk om eit vidt spekter av saker.</p>
				<h3>Kva kan du ikkje fremje saker om?</h3>
				<ul>
				<li>Saker som allereie er handsama av kommunestyret (eller fylkestinget) i løpet av valperioden.</li>
				<li>Saker med same innhald som eit tidlegare innbyggarinitiativ.</li>
				</ul>
				<p>Dette tyder ikkje at det er forbode å fremje ”den same” saka i løpet av ein og same valperiode, eller at kommunestyret eller fylkestinget ikkje har lov til å handsame saka. Kommunestyret eller fylkestinget kan velje å handsame ei sak som har blitt handsama før, men initiativtakarane kan ikkje krevje dette.</p>
				<h3>Når må saka handsamast?</h3>
				<p>Kommunestyret eller fylkestinget skal ta stilling til saka seinast seks månader etter at ho har fått nok underskrifter. Det er eitt unntak frå denne fristen. Seksmånadersfristen gjeld ikkje viss saka blir vist til handsaming i samband med ei pågåande plansak etter plan- og bygningslova.</p>
				<h3>Kor lenge kan ei sak bli liggande?</h3>
				<p>Saker som ikkje har nok underskrifter etter 12 månader, blir sletta frå misak.no.</p>
