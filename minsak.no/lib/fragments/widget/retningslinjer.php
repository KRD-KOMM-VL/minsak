				<h3>Hva er innbyggerinitiativ?</h3>
				<p>Gjennom å samle underskrifter har du som innbygger i en kommune eller et fylke rett til å sette en sak på den politiske dagsorden. Du har imidlertid ikke krav på noe bestemt utfall av saken.</p>
				<h3>Hvem kan foreslå saker?</h3>
				<p>Du må bo i den kommunen eller det fylket saken gjelder for å foreslå eller skrive under på en sak. Ungdom under 18 år kan foreslå saker.</p>
				<h3>Hvor mange underskrifter trenger du?</h3>
				<p>Du må samle underskrifter fra minst to prosent av innbyggerne, eller minimum 300 personer i kommunen/500 personer i fylket.</p>
				<h3>Hva kan du fremme saker om?</h3>
				<p>Du kan fremme saker som gjelder kommunens eller fylkeskommunens virksomhet. I praksis betyr dette oppgaver som kommunen eller fylkeskommunen er engasjert i, både lovpålagte og frivillige oppgaver. Det er dermed snakk om et vidt spekter av saker.</p>
				<h3>Hva kan du ikke fremme saker om?</h3>
				<ul>
				<li>Saker som allerede er behandlet av kommunestyret (eller fylkestinget) i løpet av valgperioden.</li>
				<li>Saker med samme innhold som et tidligere innbyggerinitiativ.</li>
				</ul>
				<p>Dette betyr ikke at det forbudt å fremme ”den samme” saken i løpet av én og samme valgperiode, eller at kommunestyret eller fylkestinget ikke har lov til å behandle saken. Kommunestyret eller fylkestinget kan velge å behandle en sak som har blitt behandlet før, men initiativtakerne kan ikke kreve dette.</p>
				<h3>Når må saken behandles?</h3>
				<p>Kommunestyret eller fylkestinget skal ta stilling til saken senest seks måneder etter at den har fått nok underskrifter. Det er ett unntak fra denne fristen. Seksmånedersfristen gjelder ikke hvis saken blir henvist til behandling i forbindelse med en pågående plansak etter plan- og bygningsloven.</p>
				<h3>Hvor lenge kan en sak bli liggende?</h3>
				<p>Saker som ikke har nok underskrifter etter 12 måneder, blir slettet fra minsak.no.</p>
