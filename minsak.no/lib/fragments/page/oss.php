		<div class="area">

			<h1>Ofte stilte spørsmål</h1>
            
            <h2>Bør underskrifter modereres?</h2>
			<p>Ja. Erfaringene tilsier at det er enkelte i andre kommuner som støtter saken. Men dette er ikke gyldige underskrifter. De bør derfor fjernes. I hvert fall når det ikke er et tilstrekkelig antall underskrifter fra kommunens egne innbyggere. Da bør initiativet være åpent for nye underskrifter helt til det har fått nok underskrifter fra innbyggerne i kommunen.</p>
            
            <h2>Hva gjør jeg når jeg har oppnådd nok underskrifter?</h2>
            <p>Som initiativtaker har du anledning til å godkjenne og avslå underskrifter. Du vil sannsynligvis ha fått enkelte underskrifter som ikke er gyldige fordi de kommer fra innbyggere utenfor kommunen eller fylkeskommunen du bor i. Disse bør forkastes. Hvis du etter å ha forkastet dem ikke har nok underskrifter til at du kan kreve saken behandlet i kommunen, kan du holde saken åpen for flere underskrifter helt du har nok underskrifter. Når du så har nok gyldige underskrifter går du inn der du moderer underskrifter og finner en knapp som sier ”send initiativet til kommunen eller fylket”. Du klikker på denne knappen og initiativet vil bli sendt til kommunen som e-post med alle underskriftene. Du vil få en kopi av e-posten som er sendt til kommunen.</p>
            
            <h2>Kan kommunen avvise at en sak behandles i kommunestyret?</h2>
            <p>Ja, kommunen kan avvise en sak for behandling i kommunestyret. Det kan skje når saken har vært oppe til behandling tidligere i kommunestyreperioden. Det kan også skje når saken ligger utenfor kommunens virksomhetsområde (det kan for eksempel være fylkeskommunen eller staten som har ansvaret for en veistrekning som man vil ha utbedret). Saken kan også avvises når det ikke er nok underskrifter fra innbyggerne.</p>
            
            <h2>Jeg kommer ikke inn på en sak for å underskrive på saken?</h2>
            <p>Da kan årsaken være at den er lukket. Dersom sluttdatoen som er oppgitt for signering ikke er passert og saken har fått nok underskrifter, kan saken være sendt inn til kommunen. Eller perioden som er fastsatt for underskriving kan være gått ut.</p>
            
            <h2>Må bildefunksjonen brukes?</h2>
            <p>Nei. Men saken blir gjort mer synlig når man kan illustrere den. Man må ikke bruke den billedtjenesten som ligger der. Man kan også laste opp sitt eget bilde for å illustrere saken.</p>
            
            <h2>Hvorfor er det ikke større krav til identifisering, for eksempel bruk av MinId når man skal skrive under?</h2>
            <p>Dette skal være et lavterskeltilbud. Det skal være enkelt å opprette en sak og det skal være enkelt å skrive under. Det skal være på linje med om det er et forslag fremmet på papir. Det krever en innsats både fra initiativtaker og kommunen å sjekke om underskriftene kommer fra innbyggere i kommunen. Derfor bes de som skriver under, om å oppgi adresse og postnummer. Initiativtakeren bør derfor også fjerne underskrifter fra personer som ikke bor i kommunen.</p>
            
            <h2>Kan jeg sette i gang innbyggerinitiativ i andre kommuner/fylkeskommuner enn i kommunen/fylkeskommunen du bor i?</h2>
            <p>Nei, du kan bare sette i gang innbyggerinitiativ i kommunen/fylkeskommunen du bor i.</p>
            
            <h2>Jeg får ikke underskrifter på saken min. Hva kan grunnen være?</h2>
            <p>Bortsett fra at det kanskje ikke er så mange som vil støtte saken, er trolig den viktigste grunnen at du ikke har klart å synliggjøre saken din for flere. Du kan spre saken din både ved å bruke Facebook, Twitter, Google+ og e-post. Når du videreformidler sake på disse mediene kan det være en idé at du oppfordrer vennene dine til å videreformidle til sine venner.</p>
            
            <h2>Hvor lenge kan en sak ligge ute for å få inn underskrifter?</h2>
            <p>Systemet setter en grense på et år. Det er forøvrig opp til den som tar initiativet å bestemme hvor lenge saken skal være åpen for signering.</p>
            
            <h2>Hvordan skal jeg gjøre det klart hvor lenge en sak skal ligge ute?</h2>
            <p>Det vil automatisk bli lagt inn en frist på 30 dager for å skrive under.  Vil man ha lenger eller kortere tid kan man endre datoen ved å fylle direkte inn eller bruke kalenderfunksjonen.</p>
            
            <h2>Kan saker endres etter at de er opprettet?</h2>
            <p>Nei, systemet tillater ikke det. Grunnen er at innholdet i en sak skal ikke kunne endres etter at det er begynt å komme inn underskrifter. Folk som skriver under skal vite at saken ikke er blitt endret etter at de har skrevet under.</p>
            
            <h2>Hva gjør jeg dersom jeg har gjort en feil når jeg opprettet saken?</h2>
            <p>Har du gjort en feil, for eksempel skrevet inn feil sakstittel, vil vi tilrå at du sletter saken og oppretter den på nytt.</p>
            
            <h2>Jeg har samlet inn underskrifter både via minsak.no og på papir og har fått til sammen nok underskrifter.</h2>
            <p>Minsak.no som løsning er basert på at man samler inn underskrifter elektronisk og du kan da ikke sende inn underskriftene til kommunen eller fylkeskommunen før du har fått det antallet elektroniske underskrifter som er nødvendig for å få saken opp i kommunestyret. Det du kan gjøre dersom du sammen med papirunderskriftene har nok underskrifter, er å logge deg inn som bruker og gå inn der det står ”Forkaste og godkjenne underskrifter”. Du får da opp lista over dem som har skrevet under. Etter at du da eventuelt har forkastet underskrifter, kan du skrive de gyldige elektroniske underskriftene ut på papir og sende dem sammen med papirunderskriftene til kommunen eller fylkeskommunen.</p>
            
            <h2>Kan en side åpnes igjen når tiden for signering er gått ut?</h2>
            <p>Nei, vi tilbyr ikke denne type service. Derfor er det viktig at den som tar initiativ tenker godt igjennom hvor lang tid han eller hun trenger for å samle nok underskrifter. Det kan også være lurt å tenke igjennom en strategi for innsamling av underskrifter, for eksempel ved aktiv bruk av for eksempel Facebook og e-post for å gjøre saken kjent gjennom venner og kjente. Vi har eksempel på at det har blitt samlet 300 underskrifter på mindre enn en dag ved hjelp av aktiv spredning av saken via nettet.</p>

