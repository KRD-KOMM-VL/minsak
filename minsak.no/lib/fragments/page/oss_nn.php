			<h1>Ofte stilte spørsmål</h1>
            
            <h2>Bør underskrifter modererast?</h2>
            <p>Ja. Erfaringane tilseier at det er enkelte i andre kommunar som støttar saka. Men dette er ikkje gyldige underskrifter. Dei bør derfor fjernast. I alle fall når det ikkje er nok underskrifter frå innbyggjarane i kommunen. Da bør initiativet vere ope for nye underskrifter heilt til det har fått nok underskrifter frå innbyggjarane i kommunen.</p>

            <h2>Kva gjer eg når eg har oppnådd nok underskrifter?</h2> 
            <p>Som initiativtakar har du høve til å godkjenne og avslå underskrifter. Du vil sannsynlegvis ha fått enkelte underskrifter som ikkje er gyldige fordi dei kjem frå innbyggjarar utanfor kommunen eller fylkeskommunen du bur i. Desse bør forkastast. Viss du etter å ha forkasta dei ikkje har nok underskrifter til at du kan krevje saka behandla i kommunen, kan du halde saka open for fleire underskrifter heilt du har nok underskrifter. Når du så har nok gyldige underskrifter går du inn der du moderer underskrifter og finn ein knapp som seier ”send initiativet til kommunen eller fylket”. Du klikka på denne knappen og initiativet vil bli sendt til kommunen som e-post med alle underskriftene. Du vil få ein kopi av e-posten som er sendt til kommunen.</p>
            
            <h2>Kan kommunen avvise at ei sak vert behandle i kommunestyret?</h2>
            <p>Ja, kommunen kan avvise ei sak for behandling i kommunestyret. Det kan skje når saka har vore oppe til behandling tidlegare i kommunestyreperioden. Det kan også skje når saka ligg utanfor virkeområde til kommunen (det kan for eksempel vere fylkeskommunen eller staten som har ansvaret for ei vegstrekning som ein vil rusta opp). Saka kan også avvisast når det ikkje er nok underskrifter frå innbyggjarar busett i kommunen.</p>
            
            <h2>Eg kjem ikkje inn på ei sak for å underskrive på saka?</h2>
            <p>Da kan årsaka vere at den er lukka. Dersom sluttdatoen som er oppgitt for signering ikkje er passert og saka har fått nok underskrifter, kan saka vere sendt inn til kommunen. Eller perioden som er fastsett for underskriving kan vere gått ut.</p> 
            
            <h2>Må bildefunksjonen brukast?</h2>
            <p>Nei. Men saka blir gjort meir synleg når ein kan illustrere den. Ein må ikkje bruke den bildetenesta som ligg der. Ein kan også laste opp sitt eige bilete for å illustrere saka.</p>
            
            <h2>Kvifor er det ikkje større krav til identifisering, for eksempel bruk av MinId når ein skal skrive under?</h2>
            <p>Dette skal vere eit tilbod med låg terskel. Det skal vere enkelt å opprette ei sak og det skal vere enkelt å skrive under. Det skal vere på linje med om det er eit forslag fremja på papir. Det krev ein innsats både frå initiativtakar og kommunen å sjekke om underskriftene kjem frå innbyggjarar i kommunen. Derfor vert dei som skriv under bedt om adresse og postnummer. Initiativtakaren bør derfor også fjerne underskrifter frå personar som ikkje bur i kommunen.</p>
            
            <h2>Kan du setje i gang innbyggjarinitiativ i andre kommunar/fylkeskommunar enn i kommunen/fylkeskommunen du bur i?</h2>
            <p>Nei, du kan berre setje i gang innbyggjarinitiativ i kommunen/fylkeskommunen du bur i.</p> 
            
            <h2>Eg får ikkje underskrifter på saka mi. Kva kan grunnen vere?</h2>
            <p>Bortsett frå at det kanskje ikkje er så mange som vil støtte saka, er truleg den viktigaste grunnen at du ikkje har klart å synleggjera saka di for fleire. Du kan spreie saka di både ved å bruke Facebook, Twitter, Google+ og e-post. Når du vidareformidlar saka på desse media kan det vere ein idé at du oppfordrar venene dine til å vidareformidle til sine vener.</p> 
            
            <h2>Kor lenge kan ei sak liggje ute for å få inn underskrifter?</h2>
            <p>Systemet set ei grense på eit år. Det er bortsett for det opp til den som tek initiativet å bestemme kor lenge saka skal vere open for signering.</p> 
            
            <h2>Korleis gjere klart kor lenge ei sak skal liggje ute?</h2>
            <p>Det vil automatisk bli lagt inn ein frist på 30 dagar for å skrive under.  Vil ein ha lengre eller kortare tid kan ein endre datoen ved å fylle direkte inn eller bruke kalenderfunksjonen.</p> 
            
            <h2>Kan saker endrast etter at dei er oppretta?</h2>
            <p>Nei, systemet tillet ikkje det. Grunnen er at innhaldet i ei sak skal ikkje kunne endrast etter at det er begynt å kome inn underskrifter. Folk som skriv under skal vite at saka ikkje er blitt endra etter at dei har skrive under.</p> 
            
            <h2>Kva gjer eg dersom eg har gjort ein feil når eg oppretta saka?</h2>
            <p>Har du gjort ein feil, for eksempel skrive inn feil sakstittel, vil vi tilrå at du slettar saka og opprettar ho på nytt.</p> 
            
            <h2>Eg har samla inn underskrifter både via misak.no og på papir og har fått til saman nok underskrifter.</h2> 
            <p>Misak.no som løysing er basert på at ein samlar inn underskrifter elektronisk og du kan då ikkje sende inn underskriftene til kommunen eller fylkeskommunen før du har fått det talet elektroniske underskrifter som er nødvendig for å få saka opp i kommunestyret. Det du kan gjere dersom du saman med papirunderskriftene har nok underskrifter, er å logge deg inn som brukar og gå inn der det står ”Forkaste og godkjenne underskrifter”. Du får da opp lista av over dei som har skrive under. Etter at du då eventuelt har forkasta underskrifter, kan du skrive dei ut på papir og sende dei saman med papirunderskriftene til kommunen.</p>  
            
            <h2>Kan ei side opnast igjen når tida for signering er gått ut?</h2>
            <p>Nei, vi tilbyr ikkje denne type service. Derfor er det viktig at den som tek initiativ tenkjer godt igjennom kor lang tid han eller ho treng for å samle nok underskrifter. Det kan også vere lurt å tenke igjennom ein strategi for innsamling av underskrifter, for eksempel aktiv bruk av t.d. Facebook og e-post for å gjere saka kjent gjennom venner og kjente. Vi har eksempel på at det har blitt samla 300 underskrifter på mindre enn ein dag ved hjelp av spreiing av saka via nettet.</p>
            
